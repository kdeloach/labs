func MostCalories(calories string) int {
	sum := 0
	hi := 0
	lines := strings.Split(calories, "\n")
	for _, line := range lines {
		if line == "" {
			if sum > hi {
				hi = sum
			}
			sum = 0
		} else {
			n, err := strconv.Atoi(line)
			if err != nil {
				panic(err)
			}
			sum += n
		}
	}
	if sum > hi {
		hi = sum
	}
	return hi
}

func Top3Calories(calories string) int {
	sum := 0
	cals := []int{}
	lines := strings.Split(calories, "\n")
	for _, line := range lines {
		if line == "" {
			cals = append(cals, sum)
			sum = 0
		} else {
			n, err := strconv.Atoi(line)
			if err != nil {
				panic(err)
			}
			sum += n
		}
	}
	cals = append(cals, sum)

	sort.Ints(cals)
	top3 := cals[len(cals)-3:]

	sum = 0
	for _, n := range top3 {
		sum += n
	}
	return sum
}

